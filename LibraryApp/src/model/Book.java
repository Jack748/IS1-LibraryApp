
package model;

/**
 *
 * @author ludov
 */
public class Book {
    String title;
    String category;
    int year;
    String author;
    
    Book(String title,String category,int year,String author){
        this.title = title;
        this.category = category;
        this.year = year;
        this.author = author;
    }
    
    public String getTitle(){
        return title;
    }
    
    public String getCategory(){
        return title;
    }
    
    public String getYear(){
        return title;
    }
    
    public String getAuthor(){
        return title;
    }
    

}
